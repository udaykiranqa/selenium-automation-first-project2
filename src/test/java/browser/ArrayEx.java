package browser;

public class ArrayEx {
	
	public static void main(String[] args) {
		
		//set of values into a single var.
		//array - means.
		
		int a[]= {10, 20, 30, 40, 50, 50, 70, 80};
	
		int count = a.length; //find the size of array.
		System.out.println("array count is : " + count);
				
		//index position.
		for (int i = 0; i < count; i++) {
			System.out.println(a[i]);
		}
		
		//array - sorting.
		
		
		
		
		
		
	}
	

}
